    @Override
    public boolean add(Object ime){
        if(glava==null){
            glava = new Clan(ime.toString());
            return true;
        }
        Clan sledeci = glava;
        while(sledeci.clan!=null){
            sledeci = sledeci.clan;
        }
        sledeci.clan = new Clan(ime.toString());
        return true; 
        }
    @Override
    public String get(int index){
        Clan sledeci = glava;
        if(index==0){
            return sledeci.vrednost;
        }
        int brojac = 0;
        while(sledeci!=null){
            if(brojac++==index){
                return sledeci.vrednost;
            }
            sledeci = sledeci.clan;
        }
        return null;
    }
    public void show(){
        Clan sledeci = glava;
        while(sledeci!=null){
            System.out.println(sledeci.vrednost);
            sledeci = sledeci.clan;
        }
    }

    @Override
    public int size() {
        int komada = 0;
        Clan sledeci = glava;
        while(sledeci!=null){
            komada ++;
            sledeci = sledeci.clan; 
        }
        return komada;
    }

